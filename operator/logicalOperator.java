package operator;

public class logicalOperator {
        public static void main(String[] args) {
            int a = 8;
            int b = 12;
    
    
            System.out.println("a > 5 && b < 15: " + (a > 5 && b < 15)); 
    

            System.out.println("a < 5 || b < 15: " + (a < 5 || b < 15)); 
    
    
            System.out.println("!(a == b): " + !(a == b));               
        }
    }
    
    
